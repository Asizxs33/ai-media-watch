# ---------------------------------------------------------------------------
# Train
# ---------------------------------------------------------------------------
texts = [d[0] for d in TRAINING_DATA]
labels = [d[1] for d in TRAINING_DATA]

model = Pipeline([
    ('tfidf', TfidfVectorizer(
        analyzer='word',
        ngram_range=(1, 2),
        max_features=20000,
        sublinear_tf=True,
        min_df=1,
    )),
    ('clf', LogisticRegression(
        max_iter=2000,
        C=5.0,
        solver='lbfgs',
    )),
])

model.fit(texts, labels)

# Cross-validation
cv_scores = cross_val_score(model, texts, labels, cv=5, scoring='f1_macro')
print(f"\nCross-validation F1 (macro): {cv_scores.mean():.3f} ± {cv_scores.std():.3f}")
print("\nClassification report (train set):")
print(classification_report(labels, model.predict(texts)))

# Save
out_dir = os.path.dirname(__file__)
model_path = os.path.join(out_dir, 'model.pkl')
joblib.dump(model, model_path)
print(f"Model saved -> {model_path}")

# Save class info
meta = {
    "classes": list(model.classes_),
    "n_samples": len(texts),
    "cv_f1_mean": float(cv_scores.mean()),
}
with open(os.path.join(out_dir, 'model_meta.json'), 'w') as f:
    json.dump(meta, f, indent=2)
print("Meta saved -> model_meta.json")
